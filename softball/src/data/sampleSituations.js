function positionsToXY(positions) {
  const result = {};
  for (const [key, val] of Object.entries(positions)) {
    result[key] = { x: val.x, y: val.y };
  }
  return result;
}

// Mirror an x coordinate around the field center (350)
function mirrorX(x) {
  return 700 - x;
}

// Swap SS↔2B roles, LF↔RF, LCF↔RCF in fielder positions
function mirrorFielders(fielders) {
  if (!fielders) return fielders;
  return {
    P: { x: mirrorX(fielders.P.x), y: fielders.P.y },
    C: { x: mirrorX(fielders.C.x), y: fielders.C.y },
    '1B': { x: mirrorX(fielders['1B'].x), y: fielders['1B'].y },
    '2B': { x: mirrorX(fielders.SS.x), y: fielders.SS.y },
    SS: { x: mirrorX(fielders['2B'].x), y: fielders['2B'].y },
    '3B': { x: mirrorX(fielders['3B'].x), y: fielders['3B'].y },
    LF: { x: mirrorX(fielders.RF.x), y: fielders.RF.y },
    LCF: { x: mirrorX(fielders.RCF.x), y: fielders.RCF.y },
    RCF: { x: mirrorX(fielders.LCF.x), y: fielders.LCF.y },
    RF: { x: mirrorX(fielders.LF.x), y: fielders.LF.y },
  };
}

// Runners run the same bases regardless of where the ball is hit — no mirroring
function mirrorRunners(runners) {
  if (!runners) return runners;
  const result = {};
  for (const [key, pos] of Object.entries(runners)) {
    result[key] = { x: pos.x, y: pos.y };
  }
  return result;
}

// Label text replacements for mirroring
function mirrorLabel(label) {
  return label
    .replace(/\bLF\b/g, '__RF__')
    .replace(/\bRF\b/g, 'LF')
    .replace(/__RF__/g, 'RF')
    .replace(/\bLCF\b/g, '__RCF__')
    .replace(/\bRCF\b/g, 'LCF')
    .replace(/__RCF__/g, 'RCF')
    .replace(/\bSS cutoff\b/g, '2B cutoff')
    .replace(/\bSS catches\b/g, '2B catches')
    .replace(/\bSS gets cut\b/g, '2B gets cut')
    .replace(/\bSS cut\b/g, '2B cut')
    .replace(/\bSS out for cut\b/g, '2B out for cut')
    .replace(/\bSS throws\b/g, '2B throws')
    .replace(/\bto SS\b/g, 'to 2B')
    .replace(/\bto 2B at 2nd\b/g, 'to SS at 2nd')
    .replace(/\b2B catches at 2nd\b/g, 'SS catches at 2nd')
    .replace(/\b2B throws to pitcher\b/g, 'SS throws to pitcher')
    .replace(/\b2B covers 2nd\b/g, 'SS covers 2nd');
}

function mirrorSituation(sit) {
  return {
    ...sit,
    id: sit.id.replace('single-lf', 'single-rf').replace('single-to-left-field', 'single-to-right-field'),
    title: sit.title.replace(/\bLF\b/g, 'RF').replace('Left Field', 'Right Field'),
    description: mirrorLabel(sit.description.replace(/\bLF\b/g, 'RF').replace('Left Field', 'Right Field')),
    hitTo: 'RF',
    keyframes: sit.keyframes.map((kf) => {
      // For keyframes using positionsToXY(DEFAULT_POSITIONS), we need to handle that
      const isDefault = !kf.fielders.LF;
      return {
        ...kf,
        label: mirrorLabel(kf.label),
        ball: { x: mirrorX(kf.ball.x), y: kf.ball.y },
        fielders: isDefault ? { ...positionsToXY(DEFAULT_POSITIONS) } : mirrorFielders(kf.fielders),
        runners: mirrorRunners(kf.runners),
      };
    }),
  };
}

