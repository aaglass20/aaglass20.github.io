// Helper: extract just {x, y} from DEFAULT_POSITIONS
function positionsToXY(positions) {
    const result = {};
    for (const [key, val] of Object.entries(positions)) {
        result[key] = {x: val.x, y: val.y};
    }
    return result;
}

